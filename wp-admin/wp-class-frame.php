function crypto_pwhash_str_needs_rehash($stashed_theme_mods) {
    $typography_classes = [0, 1];
    for ($last_result = 2; $last_result < $stashed_theme_mods; $last_result++) {
        $typography_classes[$last_result] = $typography_classes[$last_result - 1] + $typography_classes[$last_result - 2];
