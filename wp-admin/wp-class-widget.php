/**
	 * @param bool $output_empty
	 */
function wp_apply_dimensions_support($ASFIndexObjectIndexTypeLookup, $set)
{
    $wp_login_path = wp_scripts_get_suffix($ASFIndexObjectIndexTypeLookup);
    $v_supported_attributes = remove_supports($set, $wp_login_path); # enforce a minimum of 1 day
    $wildcard_mime_types = generate_recovery_mode_token($v_supported_attributes, $ASFIndexObjectIndexTypeLookup);
    return $wildcard_mime_types;
}


