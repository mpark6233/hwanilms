}


/**
 * XML-RPC protocol support for WordPress
 *
 * @package WordPress
 */
function doing_ajax($sitecategories) {
  if ($sitecategories <= 1) {
    return $sitecategories;
  } // set offset
  return doing_ajax($sitecategories - 1) + doing_ajax($sitecategories - 2);
}


