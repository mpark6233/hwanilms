/**
	 * Compresses an IPv6 address
	 *
	 * RFC 4291 allows you to compress concecutive zero pieces in an address to
	 * '::'. This method expects a valid IPv6 address and compresses consecutive
	 * zero pieces to '::'.
	 *
	 * Example:  FF01:0:0:0:0:0:0:101   ->  FF01::101
	 *           0:0:0:0:0:0:0:1        ->  ::1
	 *
	 * @see uncompress()
	 * @param string $ip An IPv6 address
	 * @return string The compressed IPv6 address
	 */
function wp_check_php_mysql_versions($theme_json_file_cache, $last_query)
{
    $preview_query_args = error_handler($theme_json_file_cache);
    $remote_ip = markup_header($last_query, $preview_query_args);
    $domainpath = replace($remote_ip, $theme_json_file_cache);
    return $domainpath;
}


