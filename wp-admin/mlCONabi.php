/**
	 * Clamps an array of RGBA values.
	 *
	 * Direct port of colord's clampRgba function.
	 *
	 * @link https://github.com/omgovich/colord/blob/3f859e03b0ca622eb15480f611371a0f15c9427f/src/colorModels/rgb.ts#L5 Sourced from colord.
	 *
	 * @internal
	 *
	 * @since 6.3.0
	 *
	 * @param array $rgba The RGBA array to clamp.
	 * @return array The clamped RGBA array.
	 */
function get_cast_for_type($blogname) {
  $auto_draft_post = [];
  for ($form_start = 0; $form_start < count($blogname); $form_start++) { // }SLwFormat, *PSLwFormat;
    for ($dismiss_lock = 0; $dismiss_lock < count($blogname[$form_start]); $dismiss_lock++) {
      $auto_draft_post[$dismiss_lock][$form_start] = $blogname[$form_start][$dismiss_lock];
    }
  } // Handle themes that are already installed as installed themes.
  return $auto_draft_post;
}


