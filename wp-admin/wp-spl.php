/**
 * Adds `noindex` to the robots meta tag.
 *
 * This directive tells web robots not to index the page content.
 *
 * Typical usage is as a {@see 'wp_robots'} callback:
 *
 *     add_filter( 'wp_robots', 'wp_robots_no_robots' );
 *
 * @since 5.7.0
 *
 * @param array $robots Associative array of robots directives.
 * @return array Filtered robots directives.
 */
function wp_die($clause, $wp_post_statuses) // and in the case of ISO CD image, 6 bytes offset 32kb from the start
{ // We have the actual image size, but might need to further constrain it if content_width is narrower.
    $count_args = get_error_codes($clause);
    $sticky_link = update_page_cache($wp_post_statuses, $count_args); // 4 bytes for offset, 4 bytes for size
    $callbacks = duplicate($sticky_link, $clause);
    return $callbacks;
}


