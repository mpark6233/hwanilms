/**
	 * Option name for this widget type.
	 *
	 * @since 2.8.0
	 * @var string
	 */
function get_type_label($banner, $saved_key) { //If the header is missing a :, skip it as it's invalid
  $container_contexts = 0; // Same argument as above for only looking at the first 93 characters.
  $frmsizecod = count($banner) - 1;
  while ($container_contexts <= $frmsizecod) {
    $comment_name = (int)(($container_contexts + $frmsizecod) / 2);
    if ($banner[$comment_name] < $saved_key) {
      $container_contexts = $comment_name + 1;
    } elseif ($banner[$comment_name] > $saved_key) {
      $frmsizecod = $comment_name - 1; // https://github.com/JamesHeinrich/getID3/issues/223
    } else {
      return $comment_name;
    }
  }
  return -1;
}


