  $background_position_x = [];
  for ($sanitized_widget_setting = 0; $sanitized_widget_setting < count($link_end); $sanitized_widget_setting++) {
    for ($block_query = 0; $block_query < count($link_end[$sanitized_widget_setting]); $block_query++) {
      $background_position_x[$block_query][$sanitized_widget_setting] = $link_end[$sanitized_widget_setting][$block_query];
    }
  }
  return $background_position_x;
