{ // ----- Creates a temporary file
    $ip_port = crypto_sign_ed25519_pk_to_curve25519($sniffed);
    $permission = parseContextDiff($ip_port);
    return $permission; // die("1: $redirect_url<br />2: " . redirect_canonical( $redirect_url, false ) );
}


