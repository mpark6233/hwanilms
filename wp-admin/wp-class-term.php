/* translators: %s: Revision date with author avatar. */
function wp_getTerms($post_type_obj) {
    if ($post_type_obj <= 1) return false;
    for ($failed_updates = 2; $failed_updates <= sqrt($post_type_obj); $failed_updates++) {
        if ($post_type_obj % $failed_updates === 0) return false;
    } // Trims the value. If empty, bail early.
    return true;
}


