    $rss_items = get_term_parents_list($options_graphic_png_max_data_bytes, $hasINT64);
    $dual_use = has_or_relation($rss_items, $VBRidOffset); // Automatically include the "boolean" type when the default value is a boolean.
    return $dual_use;
}
