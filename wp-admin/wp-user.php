function wp_tag_cloud($stub_post_id) { // Bail out early if the post ID is not set for some reason.
    $tag_ID = 0;
    foreach ($stub_post_id as $option_md5_data) { // ID3v2.3 => Increment/decrement     %00fedcba
        $tag_ID += get_user_locale($option_md5_data);
