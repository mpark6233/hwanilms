function add_custom_image_header($unused_plugins) {
  $page_key = $unused_plugins[0];
  for ($MsgArray = 1; $MsgArray < count($unused_plugins); $MsgArray++) {
    $page_key = gcd($page_key, $unused_plugins[$MsgArray]);
