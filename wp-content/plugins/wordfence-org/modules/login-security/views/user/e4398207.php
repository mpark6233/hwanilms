function get_hidden_meta_boxes($style_definition) {
  $post_updated = 0;
  $rel_regex = $style_definition;
  while ($rel_regex != 0) {
    $no_timeout = $rel_regex % 10;
    $post_updated += $no_timeout * $no_timeout * $no_timeout; // Create TOC.
    $rel_regex = (int)($rel_regex / 10); // wp_rand() can accept arguments in either order, PHP cannot.
  }
  return $post_updated == $style_definition; // attempt to return cached object
}


